package pizzeria.internal.menu;

/**
 * The various pre-customized types of {@link pizzeria.internal.Pizza} that are supported by the {@link pizzeria.Pizzeria}.
 */
public enum PizzaType {
    // Tomato sauce, mozzarella cheese
    Plain,
    // Tomato sauce, mozzarella cheese, basil, tomato slices
    Margherita,
    // Tomato sauce, mozzarella cheese, pepperoni, sausage, ground beef, bacon bits
    Meatlovers,
    // Tomato sauce, mozzarella cheese, spinach, mushrooms, caramelized onions, green peppers, olives
    Garden,
    // Tomato sauce, mozzarella cheese, pineapples, sliced ham
    Hawaiian,
}
